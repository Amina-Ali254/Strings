# Strings Methods

# Python provides a variety of built-in string methods that can be used to manipulate strings. Some of them are:

string = " Hello World  "
# Upper (): Converts all the characters in string to uppercase
print(string.upper())
# lower (): Converts all the characters in string to lowercase
print (string.lower())
# strip (): removes any leading and trailing whitespace characters from a string.
print(string.strip())
# split (): splits a string into a list of substrings based on a specifig delimiter.
print (string.split())
# replace (): replaces a specified substring with another substring.
print(string.replace("World", "Python"))